MERGE INTO PPFMS_CLAIMS_WORKFLOW_SERVICE_SCHEMA.PARTIAL_WITHDRAWAL_CAUSE_MASTER t
USING (
    SELECT 'DISABILITY' AS CODE,
           'Disability' AS NAME,
           'Unemployment caused by disability' AS DESCRIPTION
    FROM DUAL

    UNION ALL

    SELECT 'PANDEMIC' AS CODE,
           'Pandemic' AS NAME,
           'Unemployment caused by pandemic-related impact' AS DESCRIPTION
    FROM DUAL

    UNION ALL

    SELECT 'NATURAL_DISASTER' AS CODE,
           'Natural Disaster' AS NAME,
           'Unemployment caused by natural disaster' AS DESCRIPTION
    FROM DUAL
) src
ON (t.CODE = src.CODE)
WHEN NOT MATCHED THEN
INSERT (
    CODE,
    NAME,
    DESCRIPTION,
    CREATED_BY
)
VALUES (
    src.CODE,
    src.NAME,
    src.DESCRIPTION,
    'SYSTEM'
);
