CREATE TABLE PPFMS_CLAIMS_WORKFLOW_SERVICE_SCHEMA.SPECIAL_CASE_REFUND_AUTHORITY_MASTER (
    ID              NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CODE            VARCHAR2(50) NOT NULL UNIQUE,
    NAME            VARCHAR2(150) NOT NULL,
    DESCRIPTION     VARCHAR2(300),
    DISPLAY_ORDER   NUMBER,
    IS_ACTIVE       CHAR(1) DEFAULT 'Y' CHECK (IS_ACTIVE IN ('Y','N')),
    CREATED_BY      VARCHAR2(100),
    CREATED_AT      TIMESTAMP DEFAULT SYSTIMESTAMP,
    UPDATED_BY      VARCHAR2(100),
    UPDATED_AT      TIMESTAMP DEFAULT SYSTIMESTAMP
);
MERGE INTO PPFMS_CLAIMS_WORKFLOW_SERVICE_SCHEMA.SPECIAL_CASE_REFUND_AUTHORITY_MASTER t
USING (
    SELECT 'KASHO' AS CODE, 'Kasho' AS NAME, 'Royal command authority' AS DESCRIPTION, 1 AS DISPLAY_ORDER FROM DUAL
    UNION ALL SELECT 'ROYAL_EDICT', 'Royal Edict', 'Royal decree authority', 2 FROM DUAL
    UNION ALL SELECT 'COMPETENT_AUTHORITY', 'Competent Authority', 'Authorized administrative authority', 3 FROM DUAL
    UNION ALL SELECT 'COURT_ORDER', 'Court Order', 'Judicial authority decision', 4 FROM DUAL
    UNION ALL SELECT 'OTHER', 'Other Authority', 'Other exceptional authority', 5 FROM DUAL
) src
ON (t.CODE = src.CODE)

WHEN NOT MATCHED THEN
INSERT (
    CODE,
    NAME,
    DESCRIPTION,
    DISPLAY_ORDER,
    CREATED_BY
)
VALUES (
    src.CODE,
    src.NAME,
    src.DESCRIPTION,
    src.DISPLAY_ORDER,
    'SYSTEM'
);

