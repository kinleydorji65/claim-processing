    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    CATEGORY_ID VARCHAR2(50) NOT NULL,
    REASON_ID NUMBER NOT NULL,

    MAX_WITHDRAWAL_PERCENTAGE NUMBER(5,2) NOT NULL,
    NUMBER_OF_CONTRIBUTION_MONTHS NUMBER NOT NULL,

    PF_ACCUMULATION CHAR(1) DEFAULT 'N'
        CHECK (PF_ACCUMULATION IN ('Y','N')),
    TOTAL_ACCUMULATION_VALUE CHAR(1) DEFAULT 'Y'
        CHECK (TOTAL_ACCUMULATION_VALUE IN ('Y','N')),

    IS_ACTIVE CHAR(1) DEFAULT 'Y'
        CHECK (IS_ACTIVE IN ('Y','N')),

    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP,
    CREATED_BY VARCHAR2(100),
    UPDATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP,
    UPDATED_BY VARCHAR2(100),

    CONSTRAINT FK_PARTIAL_WITHDRAWAL_RULE_CATEGORY
        FOREIGN KEY (CATEGORY_ID)
        REFERENCES PPFMS_MASTER_SERVICE_SCHEMA.AGENCY_CATEGORIES (CATEGORY_ID),

    CONSTRAINT FK_PARTIAL_WITHDRAWAL_RULE_REASON
        FOREIGN KEY (REASON_ID)
        REFERENCES PPFMS_CLAIMS_WORKFLOW_SERVICE_SCHEMA.PARTIAL_WITHDRAWAL_REASON_MASTER (ID),

    CONSTRAINT CHK_PARTIAL_WITHDRAWAL_ACCUMULATION
        CHECK (
            (PF_ACCUMULATION = 'Y' AND TOTAL_ACCUMULATION_VALUE = 'N')
            OR
            (PF_ACCUMULATION = 'N' AND TOTAL_ACCUMULATION_VALUE = 'Y')
        ),

    CONSTRAINT UK_PARTIAL_WITHDRAWAL_RULE
        UNIQUE (CATEGORY_ID, REASON_ID)
);

MERGE INTO PPFMS_CLAIMS_WORKFLOW_SERVICE_SCHEMA.PARTIAL_WITHDRAWAL_RULE_MASTER t
USING (
    /* ===========================
       UNEMPLOYMENT (50%, 60 months)
       =========================== */
    SELECT '01' AS CATEGORY_ID, 2 AS REASON_ID, 50 AS MAX_PCT, 60 AS MIN_MONTHS FROM DUAL -- CIVIL
    UNION ALL
    SELECT '03', 2, 50, 60 FROM DUAL -- PRIVATE
    UNION ALL
    SELECT '04', 2, 50, 60 FROM DUAL -- ARMED_FORCE

    /* ===========================
       BUSINESS INVESTMENT (50%, 60 months)
       =========================== */
    UNION ALL
    SELECT '01', 3, 50, 60 FROM DUAL -- CIVIL
    UNION ALL
    SELECT '03', 3, 50, 60 FROM DUAL -- PRIVATE
    UNION ALL
    SELECT '04', 3, 50, 60 FROM DUAL -- ARMED_FORCE

    /* ===========================
       HOUSING (75%, 180 months)
       =========================== */
    UNION ALL
    SELECT '01', 1, 75, 180 FROM DUAL -- CIVIL
    UNION ALL
    SELECT '03', 1, 75, 180 FROM DUAL -- PRIVATE
    UNION ALL
    SELECT '04', 1, 75, 180 FROM DUAL -- ARMED_FORCE

) src
ON (
    t.CATEGORY_ID = src.CATEGORY_ID
    AND t.REASON_ID = src.REASON_ID
)

WHEN NOT MATCHED THEN
INSERT (
    CATEGORY_ID,
    REASON_ID,
    MAX_WITHDRAWAL_PERCENTAGE,
    NUMBER_OF_CONTRIBUTION_MONTHS,
    PF_ACCUMULATION,
    TOTAL_ACCUMULATION_VALUE,
    CREATED_BY
)
VALUES (
    src.CATEGORY_ID,
    src.REASON_ID,
    src.MAX_PCT,
    src.MIN_MONTHS,
    'N',  -- PF only = No
    'Y',  -- Total accumulation = Yes
    'SYSTEM'
);

